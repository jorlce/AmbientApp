    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, String> {

        private final String mUser;
        private final String mPassword;

        UserLoginTask(String user, String password) {
            mUser = user;
            mPassword = password;
        }

        @Override
        protected String doInBackground(Void... params) {
            try {
                    final String url = "http://jorlce.ddns.net:8080/ambientService/login/"
                            + mUser + "/" + mPassword;
                    RestTemplate restTemplate = new RestTemplate();
                    restTemplate.getMessageConverters().add(new StringHttpMessageConverter());
                    String responseString = restTemplate.getForObject(url, String.class);
                    Log.d("LoginActivity",responseString);
                    return responseString;
            } catch (Exception e) {
                    Log.e("LoginActivity", e.getMessage(), e);
            }


            return null;
        }

        @Override
        protected void onPostExecute(String responseString) {
            mAuthTask = null;
            showProgress(false);

            switch (responseString) {
                case "ACCEPTED":
                    Log.d("Login", "Usuario valido");
                    Intent intent = new Intent(LoginActivity.this, WelcomeActivity.class);
                    intent.putExtra("usuario",mUser);
                    startActivity(intent);
                    break;
                default:
                    mPasswordView.setError(getString(R.string.error_invalid_login));
                    mPasswordView.requestFocus();
                    break;
            }


        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }
